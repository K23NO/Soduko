def es_valido(tablero, fila, columna):
    """ Verificar que el valor ingresado cumpla con las reglas del sudoku """
    # Verificar que el valor ingresado no se encuentre en la fila
    # verificar que no se repita al introducir la ultima fila y la ultima columna
    valor = tablero[fila][columna]
    tablero[fila][columna] = 0
    for i in range(9):
        if tablero[fila][i] == valor:
            return False
    for i in range(9):
        if tablero[i][columna] == valor:
            return False
    for i in range(3):
        for j in range(3):
            if tablero[(fila//3)*3+i][(columna//3)*3+j] == valor:
                return False
    tablero[fila][columna] = valor
    return True



def tableroaleatorio_valido_con_reglas():
    """Crea un tablero aleatorio valido con las reglas del sudoku, generando uno que este completo"""
    tablero = [[0 for i in range(9)] for j in range(9)]
    for i in range(9):
        for j in range(9):
            tablero[i][j] = random.randint(1,9)
            if not es_valido(tablero, i, j):
                tablero[i][j] = 0
    return tablero


